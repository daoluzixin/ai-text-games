# XML 输出格式约束（注入在对话末尾，优先级最高）
XML_FORMAT_PROMPT = (
    "【输出格式要求 - 必须严格遵守】\n"
    "⚠️ 你的回复必须以 < 字符开头，第一个标签就是 <scene>。\n"
    "⚠️ 严禁在 <scene> 之前输出任何文字，包括但不限于「好的」「游戏开始」「遵循您的指令」之类的开场白、确认语、说明。\n"
    "⚠️ 也严禁在 </scene> 之后追加任何文字。整段回复 = 一个完整的 <scene>...</scene>，前后无任何多余字符。\n"
    "⚠️ 严禁在 narration / dialogue / choice 等正文内部使用任何 HTML 标签（例如 <span>、</span>、<b>、<i>、<div>、<br> 等）。正文只能是纯文字，需要换行就直接换行或用多个 <narration>。\n\n"
    "格式模板如下：\n\n"
    "<scene>\n"
    "  <narration>叙事描写、环境描写、心理描写等正文内容放这里。可以有多个 narration 标签。</narration>\n"
    "  <dialogue speaker=\"角色名\">角色的对话内容</dialogue>\n"
    "  <status title=\"状态面板标题\">\n"
    "    <item label=\"标签名\" icon=\"🎭\">值</item>\n"
    "    <item label=\"好感度\" icon=\"❤️\" type=\"bar\" max=\"100\">85</item>\n"
    "  </status>\n"
    "  <choices>\n"
    "    <choice id=\"A\">选项A的描述</choice>\n"
    "    <choice id=\"B\">选项B的描述</choice>\n"
    "  </choices>\n"
    "</scene>\n\n"
    "规则：\n"
    "1. 最外层必须是 <scene> 标签\n"
    "2. <narration> 用于所有叙事、描写、旁白，可以有多段\n"
    "3. <dialogue speaker=\"名字\"> 用于角色对话，speaker 属性标明说话人\n"
    "4. <status> 用于展示状态面板（如属性、关系、物品等），内含多个 <item>：\n"
    "   - <status> 可选 title 属性给面板加标题，例如 <status title=\"主播状态\">；\n"
    "     需要多个分组时，可输出多个并列的 <status>，各自带 title。\n"
    "   - <item label=\"标签\">值</item> 是基础写法；\n"
    "   - 可选 icon 属性放一个 emoji 图标，例如 icon=\"❤️\"；\n"
    "   - 数值型属性（如好感度、血量、进度）建议用进度条：\n"
    "     <item label=\"好感度\" icon=\"❤️\" type=\"bar\" max=\"100\">85</item>，\n"
    "     其中 type=\"bar\" 表示进度条，max 是上限（默认 100），标签内的值写当前数值。\n"
    "5. <choices> 用于给玩家的选项，每个 <choice id=\"字母\"> 是一个选项\n"
    "6. 标签可以按需组合，顺序自由，但必须在 <scene> 内\n"
    "7. 不要在 XML 外输出任何文字，不要用 ```xml 代码块包裹\n"
    "8. 文本内容中如果有 < > & 字符，用 &lt; &gt; &amp; 转义"
)
